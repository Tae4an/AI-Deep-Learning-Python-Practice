# 회사 쇼핑몰 고객의 아이디에 부여된 마일리지 포인트를 딕셔너리에 저장하여 관리하기

# | 아이디 | kim | lee | han | hong | hwang |
# | --- | --- | --- | --- | --- | --- |
# | 마일리지 포인트 | 12000 | 11000 | 3000 | 5000 | 18000 |

# 미션 :

#   1.  위의 표에 나타난 아이디와 포인트를 빈 딕셔너리에 저장.

