# 1. 아이디 jang의 마일리지 7000을 추가. 
# 2. 딕셔너리에서 가장 높은 마일리지를 찾아서 출력.
# 3. 딕셔너리에서 가장 낮은 마일리지를 찾아서 출력.
# 4. 전체 딕셔너리를 출력.
